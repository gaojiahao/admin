                    </p>
                    <p>3.漫画作品单张图片尺寸过大、过小，均可能被退回。建议尺寸为800*1200像素，条漫宽度在800像素左右，对白字迹清晰，
                        否则影响阅读将被审核不通过。
                    </p>
                    <p>4.上传作品封面需要具有优秀的设计感和高质量的画面精度，请将封面原图另存成157*200像素的小图后上传。
                        封面质量过低甚至影响阅读的作品将被不予通过并建议修改。封面为内容截图、封面与
                        作品内容无关或者封面品质不高的作品，将不会放在首页展示和推广，并建议修改。
                    </p>
                    <p>5.为了方便读者查找和阅读，上传做品必须准确标注相关标签，认真填写作品简介和精简描述。
                        对于标签标注错误的作品，编辑将会予以修改或审核不通过；作品简介和作品公告有明显违规或过于随意的内容，
                        作品将被退回并建议修改。
                    </p>
                    <p>6.故事漫画，每章节页数过少（建议为8页）的作品将不被审核通过，请丰富内容后再次上传。章节过少（少于两章）
                        或页数过少（少于8页）即完结的作品，为了保障读者利益，将被审核不通过，请作者丰富作品，
